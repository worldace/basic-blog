<?php
//======================================================
// ■指定したツールを実行する (管理用)
// 
// http://127.0.0.1/basic-blog/?action=tool&tool=
// 呼び出し元: ../index.php
//======================================================


パスワードチェック();

//ツールファイル(*.php)は「action/tool」に設置する
$file = dirname(__FILE__) . "/tool/{$_GET['tool']}.php";


//入力チェック
if(preg_match("/^\./", $_GET['tool'])){
    エラー("不正なツール名です");
}
if(!file_exists($file)){
    エラー("そのツールは存在しません。");
}

//実行
include($file);
